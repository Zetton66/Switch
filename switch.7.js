let size = "L";

switch (size) {
  case "S":
    console.log("Small (36-38)");
    break;
  case "M":
    console.log("Medium (40-42)");
    break;
  case "L":
    console.log("Large (44-46)");
    break;
  case "XL":
    console.log("Extra Large (48-50)");
    break;
  default:
    console.log("Size tidak tersedia");
}
