let grade = "B";
// Tulis kode switch statement di sini

switch (grade) {
  case "A":
    console.log("Sangat Baik");
    break;
  case "B":
    console.log("Baik");
    break;
  case "C":
    console.log("Cukup");
    break;
  case "D":
    console.log("Kurang");
    break;
  case "E":
    console.log("Sangat Kurang");
    break;
  default:
    console.log("Grade tidak valid");
}
